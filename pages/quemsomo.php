 <h1>HISTORIA DA EMPRESA</h1>
 <h4>
 	

 	O Sucesso não é por acaso...<br><br>

A Fábrica de Doces caseiros atua no mercado de alimentos desde 1984.Produz doces em diversas linhas, somando mais de 150 sabores e tipos diferentes para atender e superar a expectativa das pessoas que querem ter o prazer de degustar doces deliciosos e de qualidade.<br>
Para você que é lojista, experimente ter os nossos produtos em suas prateleiras e colha os benefícios das vendas e do sucesso que temos no mercado!
<br>
A modernidade também faz diferença<br>
Temos um laboratório com modernos equipamentos para o controle de qualidade, onde é feita a avaliação durante todos os processos da produção. A nossa preocupação com a qualidade nos permite fornecer produtos mais seguros para a sua saúde.

 <br><br>
Compromisso, determinação e responsabilidade são alguns dos segredos<br><br>
Continuamos sempre crescendo através do compromisso, determinação e responsabilidade de todos os colaboradores. O resultado do trabalho está na sua satisfação. Ter você como cliente é sempre um grande prazer!
Preocupamos com todos os detalhes<br>
Cuidamos desde o plantio das frutas até a embalagem de todos os produtos. Temos uma fazenda de 75 hectares, onde cultivamos as frutas usadas na fabricação dos doces e toda a lenha usada na produção. Usamos lenha de eucalipto nas caldeiras como fonte de energia e nos preocupamos constantemente com o replantio.<br><br>


Queremos estar bem perto de você!<br>
Hoje você encontra nossos produtos em mais 470 cidades de todo o Brasil. Estamos presente em todos os estados. São lojistas e distribuidores preocupados em te entregar o melhor produto.<br>

Quando falamos perto, é perto mesmo<br><br>

O mercado nacional é bem grande e é o nosso foco principal, porém, pessoas em outros países também querem desfrutar dos nossos saborosos produtos. Após recebermos contatos de pessoas que adoraram nossos produtos e quiseram saber onde comprá-los em seus países, começamos a exportar para os Estados Unidos e este é só o começo.

 
